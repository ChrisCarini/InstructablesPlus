chrome.extension.onRequest.addListener(function(request, sender, sendResponse)
{
	if(request.method == "setTimesUsed")
	{
		console.log("changing timesUsed.localStorage to "+request.data);
		localStorage.setItem("timesUsed", request.data);
		sendResponse({msg:"The Instructables+ extension has been used a total of "+request.data+" times since "+localStorage["startDate"]+"."});
	}
	if(request.method == "getAll")
	{
		console.log("getting all varibles for extension");
		sendResponse({showsteps:localStorage["showsteps"],timesUsed:localStorage["timesUsed"],startDate:localStorage["startDate"]});
	}
});